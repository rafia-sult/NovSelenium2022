package java_code_jan_17th_2023;

public class Print_Number_From_1_to_10 {

	public static void main(String[] args) {
		// print first 10 natural numbers 
		// we represent using i as natural numbers 
		
		int i = 1;
		
		while (i<=10) {
			
			System.out.println(i); // 1 
			i++; // 2 
			// it will continues to run the loop till i becomes 100
		}
		
	}

}
