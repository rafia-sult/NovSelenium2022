package java_code_jan_17th_2023;

public class Program3 {

	// WAP to print 20 multiples of 5 in decreasing order starting from 250

	public static void main(String[] args) {

		
		int value = 250; 
		int limit = 20; 
		
		while (limit >= 0) {
			System.out.println(value);
			value = value - 5;
			limit--;
			
		}
		
		
		}
		
	}


