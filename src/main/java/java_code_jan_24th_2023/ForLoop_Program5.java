package java_code_jan_24th_2023;

public class ForLoop_Program5 {

	// Print 11 multiples of 2 in descending oder starting from 20
	// it will look like 20,18,16,14,12,10,8,6,4,2,0
	public static void main(String[] args) {
		
	for(int i=20; i>=0; i=i-2) {
		
		System.out.println(i);
		
			}
			
	}
			
		

}

	


